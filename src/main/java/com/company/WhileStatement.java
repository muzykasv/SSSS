package com.company;

public class WhileStatement {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println("Iteration i: " + i);
            i++;
        }
        int j = 0;
        do {
            System.out.println("Iteration j: " + j);
            ++j;
        } while (j < 10);
    for (int y = 0; y < 6; y++) {
        System.out.println("Iteration y: " + y);
        }
    }
}


